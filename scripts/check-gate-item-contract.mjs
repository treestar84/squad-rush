import { readFile } from "node:fs/promises"

function assertContract(condition, message) {
  if (!condition) {
    throw new Error(message)
  }
}

const gateDataSource = await readFile("src/game/data/gateData.ts", "utf8")
const gateSystemSource = await readFile("src/game/systems/GateSystem.ts", "utf8")
const gateVisualSource = await readFile("src/game/systems/GateVisualFactory.ts", "utf8")
const gameSource = await readFile("src/game/Game.ts", "utf8")
const hudSource = await readFile("src/ui/Hud.ts", "utf8")
const pickupSource = await readFile("src/game/systems/BonusPickupSystem.ts", "utf8")
const designSource = await readFile("DESIGN.md", "utf8")

assertContract(gateDataSource.includes("gate_add3"), "Gate data must include a +3 increase gate.")
assertContract(!gateDataSource.includes("SPECIAL_RECRUIT"), "Specific character recruit gates must be removed.")
assertContract(!gateDataSource.includes("PROMOTION"), "Promotion gates must be removed.")
assertContract(!gateDataSource.includes("gate_special"), "Special character gate configs must not remain.")
assertContract(!gateDataSource.includes("gate_promote"), "Promotion gate configs must not remain.")
assertContract(gateDataSource.includes("readonly [string, string]"), "Gate spawns must contain exactly two gate ids.")
assertContract(!gateDataSource.includes("readonly [string, string, string]"), "Three-choice gate spawns must be removed.")
assertContract(gateDataSource.includes("LEFT_GATE_REWARD_POOL"), "Gate data must expose the safe left reward pool.")
assertContract(gateDataSource.includes("RIGHT_GATE_REWARD_POOL"), "Gate data must expose the risky right reward pool.")
assertContract(!gateDataSource.includes("gate_mul3"), "x3 gate rewards must not remain active.")
assertContract(gateDataSource.includes("gate_attack_amp20"), "Rare permanent attack gate must be active.")
assertContract(gateDataSource.includes("gate_pangyo_damage2x"), "Rare Pangyo damage gate must be active.")
assertContract(gateSystemSource.includes("rightBarrier"), "Right-side risky gate must have a destructible barrier.")
assertContract(gateSystemSource.includes("damageRightBarrier"), "Gate system must damage the right-side wall before pass resolution.")
assertContract(gateSystemSource.includes("rightBarrier.hp <= 0"), "Right gate reward must depend on wall destruction.")
assertContract(gateSystemSource.includes("createRewardPoolGateChoices"), "Gate system must roll randomized reward pool choices.")
assertContract(gateSystemSource.includes("revealGate"), "Gate system must reveal hidden choices after barrier destruction.")
assertContract(gateSystemSource.includes('[gate.choices[0].displayText, "?"]'), "Gate debug state must keep left reward visible while the right reward is hidden.")
assertContract(!gateSystemSource.includes("sortChoices"), "Gate choices must not be sorted into guaranteed weaker-left/stronger-right order.")
assertContract(!gateSystemSource.includes("GATE_CENTER_X"), "Center gate placement must not be used.")
assertContract(gateSystemSource.includes("export type GateApplyResult"), "Gate pass results must expose applied reward deltas.")
assertContract(gateSystemSource.includes("addedUnits"), "Gate pass results must include actual added unit counts.")
assertContract(gameSource.includes("showUnitRewardFlight"), "Game gate pass handling must forward added unit rewards to HUD portrait flights.")
assertContract(hudSource.includes("showUnitRewardFlight"), "HUD must expose a grouped unit reward portrait flight.")
assertContract(hudSource.includes("for (let index = 0; index < flightCount; index += 1)"), "HUD must emit grouped duplicate portraits together instead of one callback per unit.")
assertContract(gateVisualSource.includes("createGateBarrierVisual"), "Gate visuals must include a right-side barrier with health bar.")
assertContract(gateVisualSource.includes("gate_barrier_hp_fill"), "Barrier must expose a visible HP fill mesh.")
assertContract(gateVisualSource.includes("setDisplayText"), "Gate visuals must support revealing hidden reward labels.")
assertContract(gateVisualSource.includes("text.length > 9 ? 58"), "Gate labels must use the larger reward text scale.")
assertContract(!gateVisualSource.includes("gate_portal_"), "Choice gates must not use the newer illustrated portal texture visuals.")
assertContract(gateVisualSource.includes("gate_panel_"), "Choice gates must render the restored plain color panel.")
assertContract(!gateVisualSource.includes("gate_frame_l_"), "Choice gates must not render side frame meshes.")
assertContract(!gateVisualSource.includes("gate_frame_r_"), "Choice gates must not render side frame meshes.")
assertContract(!gateVisualSource.includes("gate_frame_t_"), "Choice gates must not render top frame meshes.")
assertContract(!gateVisualSource.includes("gate_depth_plate_"), "Choice gates must not render a rear frame plate.")
assertContract(!gateVisualSource.includes("gate_overhead_beam_"), "Choice gates must not render a wood-like overhead frame.")
assertContract(!gateVisualSource.includes("gate_anchor_pylon_"), "Choice gates must not render frame anchor pylons.")
assertContract(!gateVisualSource.includes("gate_energy_ring_"), "Choice gates must not render an energy ring.")
assertContract(gateVisualSource.includes("gate_label_backplate_"), "Choice gates must render the restored plain label backplate.")
assertContract(!pickupSource.includes("pickup_crate"), "Fixed box pickup visuals must be unused.")
assertContract(!pickupSource.includes("pickup_reward_gem"), "Fixed gem/core box pickup visuals must be unused.")
assertContract(!pickupSource.includes("pickup_supply_band"), "Fixed supply-band box pickup visuals must be unused.")
assertContract(pickupSource.includes("pickup_orb_shell"), "Rotating pickup shell must stay active.")
assertContract(designSource.includes("Mystery reward pacing"), "DESIGN.md must document the mystery reward gate concept.")

console.info("Gate/item contract OK")
